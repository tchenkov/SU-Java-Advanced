public class P06_NumbersZeroToNine {
    
    private final static int END_NUM = 9;
    
    public static void main(String[] args) {
        int num = 0;
        
        while (num <= END_NUM){
            System.out.printf("Number: %d%n", num);
            num++;
        }
    }
}
